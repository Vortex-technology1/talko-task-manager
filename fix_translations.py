def i18n(ua, ru):
    """Генерує JS вираз: window.currentLang==='ru' ? 'RU' : 'UA'"""
    ua_e = ua.replace("'", "\\'")
    ru_e = ru.replace("'", "\\'")
    return f"(window.currentLang==='ru'?'{ru_e}':'{ua_e}')"

