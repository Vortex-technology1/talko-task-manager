import re, os

def get_lang_expr(ua_str, ru_str):
    """Генерує JS тернарний вираз для i18n"""
    # Якщо рядки однакові - не потрібна заміна
    if ua_str == ru_str:
        return None
    ua_e = ua_str.replace('\\', '\\\\').replace("'", "\\'")
    ru_e = ru_str.replace('\\', '\\\\').replace("'", "\\'")
    return f"(window.currentLang==='ru'?'{ru_e}':'{ua_e}')"

# Для простих рядкових констант в HTML шаблонах - пряма заміна тексту
# Зберігаємо UA текст але додаємо RU через JS window.currentLang

def process_file_simple(path, replacements):
    """Замінює тільки явні рядки які є UI-текстом в шаблонах"""
    with open(path, 'r', encoding='utf-8') as f:
        c = f.read()
    
    count = 0
    for ua, ru in replacements:
        if ua in c:
            # Перевіряємо контекст - тільки якщо в HTML/JS рядку
            expr = get_lang_expr(ua, ru)
            if expr:
                c = c.replace(f"'{ua}'", f"'{ua}'")  # поки залишаємо
            count += 1
    
    return c, count

