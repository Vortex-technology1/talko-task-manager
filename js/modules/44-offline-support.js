// =====================
        // OFFLINE SUPPORT
        // =====================
'use strict';
        let offlineData = {
            tasks: [],
            functions: [],
            users: [],
            regularTasks: []
        };
        
        // Save data to localStorage for offline access
        function saveOfflineData() {
            try {
                // Зберігаємо тільки потрібні поля — без description, timeLog, checklist
                // щоб не перевищити localStorage 5MB limit
                const trimmedTasks = tasks.map(t => ({
                    id: t.id,
                    title: t.title,
                    status: t.status,
                    assigneeId: t.assigneeId,
                    assigneeName: t.assigneeName,
                    creatorId: t.creatorId,
                    creatorName: t.creatorName,
                    deadlineDate: t.deadlineDate,
                    deadlineTime: t.deadlineTime,
                    deadline: t.deadline,
                    function: t.function,
                    priority: t.priority,
                    pinned: t.pinned,
                    estimatedTime: t.estimatedTime,
                    requireReview: t.requireReview,
                    processId: t.processId,
                    regularTaskId: t.regularTaskId,
                    autoGenerated: t.autoGenerated,
                    createdAt: t.createdAt
                }));
                
                offlineData = {
                    tasks: trimmedTasks,
                    functions: functions,
                    users: users,
                    regularTasks: regularTasks.map(rt => ({
                        id: rt.id,
                        title: rt.title,
                        function: rt.function,
                        period: rt.period,
                        daysOfWeek: rt.daysOfWeek,
                        dayOfWeek: rt.dayOfWeek,
                        dayOfMonth: rt.dayOfMonth,
                        timeStart: rt.timeStart,
                        estimatedTime: rt.estimatedTime
                    })),
                    lastSync: new Date().toISOString()
                };
                
                const json = JSON.stringify(offlineData);
                
                // Перевірка розміру перед записом (~5MB limit)
                if (json.length > 4 * 1024 * 1024) {
                    // Якщо все ще > 4MB — зберігаємо тільки останні 200 задач
                    offlineData.tasks = trimmedTasks.slice(0, 200);
                    localStorage.setItem('offlineData', JSON.stringify(offlineData));
                } else {
                    localStorage.setItem('offlineData', json);
                }
            } catch (e) {
                // QuotaExceededError — не блокуємо роботу, просто логуємо
                console.warn('saveOfflineData: localStorage full, skipping', e.name);
                try {
                    // Fallback: видаляємо offline дані щоб звільнити місце
                    localStorage.removeItem('offlineData');
                } catch (e2) { /* ignore */ }
            }
        }
        
        // Load offline data
        function loadOfflineData() {
            try {
                const saved = localStorage.getItem('offlineData');
                if (saved) {
                    offlineData = JSON.parse(saved);
                    return true;
                }
            } catch(e) {
                // Safari ITP або QuotaExceeded — продовжуємо без офлайн даних
                console.warn('[offline] loadOfflineData error:', e.name);
            }
            return false;
        }
        
        // Check online status
        function isOnline() {
            return navigator.onLine;
        }
        
        // Show offline indicator
        function updateOnlineStatus() {
            let indicator = document.getElementById('offlineIndicator');
            
            if (!isOnline()) {
                if (!indicator) {
                    indicator = document.createElement('div');
                    indicator.id = 'offlineIndicator';
                    indicator.style.cssText = 'position:fixed;top:0;left:0;right:0;height:3px;background:#f59e0b;z-index:99999;transition:opacity 0.3s;';
                    document.body.appendChild(indicator);
                }
                indicator.style.opacity = '1';
                
                // Load offline data
                if (loadOfflineData() && offlineData.tasks.length) {
                    tasks = offlineData.tasks;
                    functions = offlineData.functions;
                    users = offlineData.users;
                    regularTasks = offlineData.regularTasks;
                    renderTasks();
                }
            } else {
                if (indicator) {
                    indicator.style.opacity = '0';
                    setTimeout(() => indicator?.remove(), 300);
                }
            }
        }
        
        // Auto-save data when loaded
        const originalLoadAllData = window.loadAllData;
        if (originalLoadAllData) {
            window.loadAllData = async function() {
                const result = await originalLoadAllData();
                if (isOnline()) {
                    saveOfflineData();
                }
                return result;
            };
        }
        
        // Listen for online/offline events
        window.addEventListener('online', () => {
            updateOnlineStatus();
            // Reload fresh data
            if (typeof loadAllData === 'function') {
                loadAllData();
            }
        });
        
        window.addEventListener('offline', updateOnlineStatus);
        
        // Keyboard shortcuts
        document.addEventListener('keydown', (e) => {
            // Escape - закрити модалку
            if (e.key === 'Escape') {
                const modals = document.querySelectorAll('.modal');
                let closed = false;
                modals.forEach(modal => {
                    if (modal.style.display === 'block') {
                        // FIX: completionReportModal needs special close handler to clear lock
                        if (modal.id === 'completionReportModal' && typeof closeCompletionReport === 'function') {
                            closeCompletionReport(false);
                        } else {
                            closeModal(modal.id);
                        }
                        closed = true;
                    }
                });
                // Закрити quick menu
                closeTaskQuickMenu();
                // Закрити user-multiselect dropdown
                document.querySelectorAll('.user-ms-dropdown').forEach(d => { d.style.display = 'none'; resetDropdownPosition(d); });
                document.querySelectorAll('.user-ms-toggle').forEach(t => t.style.borderColor = '#d1d5db');
                return;
            }
            
            // Не обробляємо якщо фокус в input/textarea
            if (e.target.tagName === 'INPUT' || e.target.tagName === 'TEXTAREA' || e.target.tagName === 'SELECT') {
                // Enter в формі - submit
                if (e.key === 'Enter' && !e.shiftKey && e.target.tagName !== 'TEXTAREA') {
                    const form = e.target.closest('form');
                    if (form) {
                        e.preventDefault();
                        form.dispatchEvent(new Event('submit', { cancelable: true }));
                    }
                }
                return;
            }
            
            // Ctrl/Cmd + shortcuts
            if (e.ctrlKey || e.metaKey) {
                switch(e.key.toLowerCase()) {
                    case 'n': // Нове завдання
                        e.preventDefault();
                        openTaskModal();
                        break;
                    case 'k': // P1 FIX: Ctrl+K обробляє module 72 (global search)
                        // НЕ обробляємо тут — module 72 має пріоритет
                        // Видалено: switchTab + taskSearchInput focus (конфлікт)
                        break;
                    case 's': // Зберегти (prevent default browser save)
                        e.preventDefault();
                        break;
                }
                return;
            }
            
            // Shortcuts без модифікаторів (тільки коли немає відкритих модалок)
            const anyModalOpen = Array.from(document.querySelectorAll('.modal')).some(m => m.style.display === 'block');
            if (anyModalOpen) return;
            
            switch(e.key) {
                case '1':
                    switchTab('myday');
                    break;
                case '2':
                    switchTab('tasks');
                    break;
                case '3':
                    switchTab('control');
                    break;
                case '4':
                    switchTab('regular');
                    break;
                case '5':
                    switchTab('functions');
                    break;
                case '/':
                    e.preventDefault();
                    const si = document.getElementById('taskSearchInput');
                    if (si) { switchTab('tasks'); setTimeout(() => si.focus(), 100); }
                    break;
                case '?':
                    // P2 FIX: кастомний modal замість alert
                    e.preventDefault();
                    if (typeof showAlertModal === 'function') {
                        showAlertModal(
                            '<b>' + window.t('hotkeysTitle') + '</b><br><br>' +
                            '<b>Ctrl+K</b> — глобальний пошук<br>' +
                            '<b>Ctrl+N</b> — нове завдання<br>' +
                            '<b>Ctrl+S</b> — заблоковано (збереження форми)<br>' +
                            '<b>1–5</b> — перемикання вкладок<br>' +
                            '<b>Escape</b> — закрити модалку<br>' +
                            '<b>/</b> — пошук у списку завдань<br>' +
                            '<b>?</b> ' + window.t('thisHelp')
                        );
                    }
                    break;
            }
        });
        
        // Check on load
        document.addEventListener('DOMContentLoaded', updateOnlineStatus);
        
        // Periodic save (every 30 seconds) — tracked for cleanup on logout
        window._offlineSaveInterval = setInterval(() => {
            if (isOnline() && tasks.length > 0) {
                saveOfflineData();
            }
        }, 30000);
